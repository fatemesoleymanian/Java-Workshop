package usc.ac.ir;

public class Savari {

    public String carName;
    public int soopapNum;
    public double acceleration;
    public int enginePower;
    public double carCost;
    public String getCarName() {
        return carName;
    }
    public void setCarName(String carName) {
        this.carName = carName;
    }
    public double getCarCost() {
        return carCost;
    }
    public void setCarCost(double carCost) {
        this.carCost = carCost;
    }
    public String engineType;
    public String breakSystem;
    public int getSoopapNum() {
        return soopapNum;
    }
    public void setSoopapNum(int soopapNum) {
        this.soopapNum = soopapNum;
    }
    public double getAcceleration() {
        return acceleration;
    }
    public void setAcceleration(double acceleration) {
        this.acceleration = acceleration;
    }
    public int getEnginePower() {
        return enginePower;
    }
    public void setEnginePower(int enginePower) {
        this.enginePower = enginePower;
    }
    public String getEngineType() {
        return engineType;
    }

    public void setEngineType(String engineType) {
        this.engineType = engineType;
    }

    public String getBreakSystem() {
        return breakSystem;
    }

    public void setBreakSystem(String breakSystem) {
        this.breakSystem = breakSystem;
    }
}
